class TestNormalizePath:
    def test_openapi_params(self):
        from rtfm.extractors.schema_extractor import _normalize_path
        assert _normalize_path("/users/{id}") == "/users/{*}"
        assert _normalize_path("/orgs/{org_id}/repos/{repo_id}") == "/orgs/{*}/repos/{*}"

    def test_express_params(self):
        from rtfm.extractors.schema_extractor import _normalize_path
        assert _normalize_path("/users/:id") == "/users/{*}"

    def test_template_params(self):
        from rtfm.extractors.schema_extractor import _normalize_path
        assert _normalize_path("/users/${userId}") == "/users/{*}"

    def test_no_params(self):
        from rtfm.extractors.schema_extractor import _normalize_path
        assert _normalize_path("/users") == "/users"

    def test_strips_trailing_slash(self):
        from rtfm.extractors.schema_extractor import _normalize_path
        assert _normalize_path("/users/") == "/users"
