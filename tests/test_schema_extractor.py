class TestOpenAPIExtraction:
    def test_extracts_endpoints_from_openapi_yaml(self, project_root):
        schema = project_root / "api" / "openapi.yaml"
        schema.parent.mkdir(parents=True)
        schema.write_text("""
openapi: "3.0.0"
info:
  title: Test API
paths:
  /users:
    get:
      operationId: listUsers
    post:
      operationId: createUser
  /users/{id}:
    get:
      operationId: getUser
""")
        result = extract(schema, project_root, {})
        # 1 schema node + 3 endpoint nodes
        assert len(result.nodes) == 4
        assert any(n["id"] == "schema::GET /users" for n in result.nodes)
        assert any(n["id"] == "schema::POST /users" for n in result.nodes)
        assert any(n["id"] == "schema::GET /users/{id}" for n in result.nodes)

    def test_extracts_endpoints_from_openapi_json(self, project_root):
        schema = project_root / "api.json"
        schema.write_text(json.dumps({
            "openapi": "3.0.0",
            "paths": {
                "/health": {"get": {"operationId": "healthCheck"}},
            },
        }))
        result = extract(schema, project_root, {})
        assert len(result.nodes) == 2
        assert any(n["id"] == "schema::GET /health" for n in result.nodes)

    def test_skips_non_openapi_yaml(self, project_root):
        config = project_root / "config.yaml"
        config.write_text("database:\n  host: localhost\n")
        result = extract(config, project_root, {})
        assert len(result.nodes) == 0

    def test_skips_invalid_json(self, project_root):
        bad = project_root / "broken.json"
        bad.write_text("{not valid json")
        result = extract(bad, project_root, {})
        assert len(result.nodes) == 0


